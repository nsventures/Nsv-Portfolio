import { writeFileSync } from 'node:fs'
import { createRequire } from 'node:module'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const require = createRequire(import.meta.url)
const { allCountries } = require('country-telephone-data')

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const outPath = join(root, 'src', 'data', 'phoneCountries.generated.ts')

const entries = allCountries.map((country) => {
  const code = country.iso2.toUpperCase()
  return {
    code,
    name: country.name.trim(),
    dialCode: country.dialCode,
    nationalLength: code === 'IN' ? 10 : [6, 12],
  }
})

const india = entries.find((entry) => entry.code === 'IN')
const rest = entries
  .filter((entry) => entry.code !== 'IN')
  .sort((a, b) => a.name.localeCompare(b.name, 'en'))
const list = india ? [india, ...rest] : rest

const file = `// Auto-generated by scripts/generate-phone-countries.mjs — run \`node scripts/generate-phone-countries.mjs\` to refresh.

export const PHONE_COUNTRIES_LIST = ${JSON.stringify(list, null, 2)} as const
`

writeFileSync(outPath, file, 'utf8')
console.log(`Wrote ${list.length} countries to ${outPath}`)
